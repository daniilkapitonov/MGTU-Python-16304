# ============================================
# ШАГ 1: ИМПОРТ МОДУЛЯ RANDOM
# ============================================
# Модуль - это дополнительная библиотека функций в Python
# Модуль random содержит функции для работы со случайными числами
# Чтобы использовать эти функции, нужно сначала импортировать модуль
# Ключевое слово import загружает модуль в нашу программу
import random

# ============================================
# ШАГ 2: ЗАГАДЫВАНИЕ СЛУЧАЙНОГО ЧИСЛА
# ============================================
# Используем функцию randint() из модуля random
# randint(a, b) генерирует случайное ЦЕЛОЕ число от a до b ВКЛЮЧИТЕЛЬНО
# То есть может выпасть 1, 2, 3, ..., 10

# Разбираем по частям:
# random.randint(1, 10) - вызываем функцию randint() из модуля random
# Она возвращает случайное число (например, 7)
# rand = ... - сохраняем это число в переменную с именем "rand"
rand = random.randint(1, 10)  # Загаданное число

# ============================================
# ШАГ 3: ВЫВОД ЗАГАДАННОГО ЧИСЛА (ДЛЯ ОТЛАДКИ)
# ============================================
# Это строка для проверки - она показывает, какое число загадано
# В реальной игре её нужно убрать, иначе игрок будет видеть ответ!
print(rand)
# Например, может вывести: 7

# ============================================
# ШАГ 4: НАЧАЛО ЦИКЛА - 3 ПОПЫТКИ УГАДАТЬ
# ============================================
# Цикл for будет повторяться 3 раза (от 0 до 2)
# Переменная i будет принимать значения: 0, 1, 2
for i in range(0, 3):
    # Код внутри цикла выполнится 3 раза (если не прервём досрочно)
    
    # ============================================
    # ШАГ 5: ВВОД ЧИСЛА ОТ ПОЛЬЗОВАТЕЛЯ
    # ============================================
    # input() показывает сообщение и ждёт ввода от пользователя
    # Пользователь вводит число (например, "5")
    # int(...) преобразует введённый текст в целое число
    # Например, текст "5" становится числом 5
    # Результат сохраняем в переменную num
    num = int(input("Введите число от 1 до 10 - "))
    
    # ============================================
    # ШАГ 6: ПРОВЕРКА - УГАДАЛ ЛИ ПОЛЬЗОВАТЕЛЬ
    # ============================================
    # Оператор if проверяет условие
    # Здесь проверяем: равно ли введённое число загаданному?
    if num == rand:
        # Если ДА (числа равны):
        # 1. Выводим сообщение о победе
        # 2. break - ПРЕРЫВАЕМ ЦИКЛ ДОСРОЧНО
        #    Выходим из цикла, следующие попытки не нужны
        print("WIN!", rand)  # Например: "WIN! 7"
        break  # Выход из цикла
    
    # ============================================
    # ШАГ 7: ПРОВЕРКА - ВВЕДЁННОЕ ЧИСЛО БОЛЬШЕ
    # ============================================
    # elif = "else if" (иначе если) - проверяем следующее условие
    # Только если первое условие (num == rand) было ЛОЖЬ
    elif num > rand:
        # Если введённое число БОЛЬШЕ загаданного
        print("Ошибся. Число меньше")
        # Цикл продолжается, будет следующая попытка
    
    # ============================================
    # ШАГ 8: ВВЕДЁННОЕ ЧИСЛО МЕНЬШЕ
    # ============================================
    # else = "иначе" - выполняется, если ВСЕ предыдущие условия ложны
    # Здесь это значит: num != rand И num <= rand
    # То есть введённое число меньше загаданного
    else:
        print("Ошибся. Число больше")
        # Цикл продолжается, будет следующая попытка

# ============================================
# ШАГ 9: ЧТО ПРОИСХОДИТ ПОСЛЕ ЦИКЛА
# ============================================
# После цикла программа завершается
# Если пользователь угадал - цикл прервался командой break
# Если не угадал за 3 попытки - цикл завершился сам
# В этом случае программа просто заканчивается без сообщения