from random import randint, choice

clothes_up = ['Рубашка', "Футболка", "Майка", "Поло"]
clothes_down = ["Шорты", "Брюки", "Штаны", "Спортивки"]
hat = ["Кепка", "Шапка", "Пусто", "Панамка"]
male = ["Муж", "Жен"]

# ============================================
# ШАГ 1: ИМПОРТ ФУНКЦИЙ ИЗ МОДУЛЯ RANDOM
# ============================================
# Модуль random содержит функции для работы со случайными числами
# from ... import ... позволяет импортировать только нужные функции,
# а не весь модуль целиком

# randint(a, b) - генерирует случайное ЦЕЛОЕ число от a до b включительно
# choice(список) - выбирает случайный элемент из списка
from random import randint, choice

# ============================================
# ШАГ 2: СОЗДАНИЕ СПИСКОВ С ОДЕЖДОЙ И АКСЕССУАРАМИ
# ============================================
# Создаём список "верхней одежды" (то, что надевается на верхнюю часть тела)
# Список - это упорядоченная коллекция элементов в квадратных скобках []
clothes_up = ['Рубашка', "Футболка", "Майка", "Поло"]
# Названия в кавычках (одинарных или двойных) - это строки (текст)

# Создаём список "нижней одежды" (то, что надевается на нижнюю часть тела)
clothes_down = ["Шорты", "Брюки", "Штаны", "Спортивки"]

# Создаём список головных уборов
# "Пусто" означает, что человек может быть без головного убора
hat = ["Кепка", "Шапка", "Пусто", "Панамка"]

# Создаём список возможных полов
male = ["Муж", "Жен"]

# ============================================
# ШАГ 3: СОЗДАНИЕ ПУСТОГО СЛОВАРЯ ДЛЯ ХРАНЕНИЯ ЛЮДЕЙ
# ============================================
# Словарь - это коллекция пар "ключ-значение"
# Ключи должны быть уникальными, значения могут повторяться
# Фигурные скобки {} обозначают пустой словарь
people = {}

# ============================================
# ШАГ 4: ЗАПРОС КОЛИЧЕСТВА ЛЮДЕЙ У ПОЛЬЗОВАТЕЛЯ
# ============================================
# input() показывает сообщение и ждёт ввода
# int(...) преобразует введённый текст в целое число
# Например, если пользователь введёт "5", int() сделает из этого число 5
num = int(input("Введите кол-во случайных людей - "))

# ============================================
# ШАГ 5: ЦИКЛ ДЛЯ СОЗДАНИЯ ЗАДАННОГО КОЛИЧЕСТВА ЛЮДЕЙ
# ============================================
# Цикл for выполнится num раз
# range(0, num) создаёт последовательность чисел от 0 до num-1
# Например, если num=3, то i будет принимать значения: 0, 1, 2
for i in range(0, num):
    
    # ============================================
    # ШАГ 6: ГЕНЕРАЦИЯ УНИКАЛЬНОГО ID ДЛЯ ЧЕЛОВЕКА
    # ============================================
    # Генерируем случайный ID от 0 до 10 миллионов
    p_id = randint(0, 10000000)
    
    # ============================================
    # ШАГ 7: ПРОВЕРКА НА УНИКАЛЬНОСТЬ ID (ЦИКЛ WHILE)
    # ============================================
    # Цикл while выполняется ПОКА условие истинно
    # people.keys() возвращает все ключи словаря people
    # Условие: p_id in people.keys() - проверяет, есть ли такой ID уже в словаре
    # Если ДА (ID уже существует), цикл будет повторяться
    while p_id in people.keys():
        # Генерируем новый случайный ID
        p_id = randint(0, 10000000)
    # Цикл закончится, когда найдётся ID, которого ещё нет в словаре
    
    # ============================================
    # ШАГ 8: СОЗДАНИЕ ХАРАКТЕРИСТИК ЧЕЛОВЕКА (СПИСКА)
    # ============================================
    # Создаём список характеристик для человека:
    # 1. randint(1, 80) - случайный возраст от 1 до 80 лет
    # 2. choice(male) - случайный пол из списка ["Муж", "Жен"]
    # 3. choice(clothes_up) - случайная верхняя одежда
    # 4. choice(clothes_down) - случайная нижняя одежда
    # 5. choice(hat) - случайный головной убор (или "Пусто")
    
    # Пример того, что может получиться: [25, "Муж", "Футболка", "Шорты", "Кепка"]
    characteristics = [
        randint(1, 80),      # возраст
        choice(male),        # пол
        choice(clothes_up),  # верхняя одежда
        choice(clothes_down),# нижняя одежда
        choice(hat)          # головной убор
    ]
    
    # ============================================
    # ШАГ 9: ДОБАВЛЕНИЕ ЧЕЛОВЕКА В СЛОВАРЬ
    # ============================================
    # Добавляем новую запись в словарь people
    # Ключ: p_id (уникальный идентификатор)
    # Значение: characteristics (список характеристик)
    # Синтаксис: словарь[ключ] = значение
    people[p_id] = characteristics
    
    # Цикл повторяется для следующего человека

# ============================================
# ШАГ 10: ВЫВОД ВСЕГО СЛОВАРЯ С ЛЮДЬМИ
# ============================================
# После завершения цикла выводим весь словарь people
# Будет показано что-то вроде:
# {1234567: [25, "Муж", "Футболка", "Шорты", "Кепка"],
#  8765432: [30, "Жен", "Рубашка", "Брюки", "Пусто"]}
print(people)

# ============================================
# ШАГ 11: ПРОВЕРКА КОРРЕКТНОСТИ СОЗДАНИЯ ЛЮДЕЙ
# ============================================
# Проверяем, совпадает ли запрошенное количество с фактическим
# num == len(people) - сравнение:
#   num - сколько людей просил пользователь
#   len(people) - сколько людей реально создано (длина словаря)
# Результат сравнения: True (истина) или False (ложь)

# print() выводит три значения через запятую:
# 1. Результат сравнения (True/False)
# 2. Запрошенное количество (num)
# 3. Фактическое количество (len(people))
print(num == len(people), num, len(people))
# Пример вывода: True 5 5