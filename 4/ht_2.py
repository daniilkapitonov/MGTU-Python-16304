# Описание:
# У вас есть список оценок студентов за 
# экзамен (от 1 до 10). Нужно:
# 1.	С помощью filter() оставить только 
# положительные оценки (5 и выше)
# 2.	С помощью map() повысить каждую 
# оценку на 1 балл (но не более 10)
# 3.	Вывести все этапы обработки
# Требования:
# 1.	Создать исходный список оценок
# 2.	Использовать filter() с лямбда-функцией
# 3.	Использовать map() с лямбда-функцией
# 4.	Преобразовать результаты в списки
# 5.	Вывести на каждом этапе

# ============================================
# ШАГ 1: СОЗДАНИЕ ИСХОДНОГО СПИСКА ОЦЕНОК
# ============================================
# Создаём список под названием "scores" (англ. "оценки" или "баллы")
# Список - это упорядоченная коллекция элементов в квадратных скобках []
# Здесь 10 чисел: 1, 4, 7, 3, 9, 10, 2, 6, 5, 8
# Это оценки студентов за экзамен (от 1 до 10 баллов)
scores = [1, 4, 7, 3, 9, 10, 2, 6, 5, 8]

# ============================================
# ШАГ 2: ВЫВОД ИСХОДНОГО СПИСКА
# ============================================
# Функция print() выводит на экран то, что находится в скобках
# Здесь она покажет наш список целиком
print(scores)
# На экране будет: [1, 4, 7, 3, 9, 10, 2, 6, 5, 8]

# ============================================
# ШАГ 3: ФИЛЬТРАЦИЯ - ОСТАВЛЯЕМ ТОЛЬКО ПОЛОЖИТЕЛЬНЫЕ ОЦЕНКИ
# ============================================
# Теперь мы будем использовать функцию filter()
# Её задача: отобрать только те элементы, которые удовлетворяют условию
# Синтаксис: filter(функция-условие, список)

# Сначала разберём лямбда-функцию:
# lambda x: x >= 5
# - "lambda" - ключевое слово для создания анонимной (безымянной) функции
# - "x" - это параметр функции (каждая оценка из списка по очереди)
# - "x >= 5" - условие: оценка должна быть больше или равна 5
#   Если условие True (истина) - элемент остаётся
#   Если False (ложь) - элемент удаляется

# Как работает filter():
# 1. Берёт первый элемент списка: 1
# 2. Проверяет: 1 >= 5? Нет (False) → удаляет
# 3. Берёт второй элемент: 4
# 4. Проверяет: 4 >= 5? Нет (False) → удаляет
# 5. Берёт третий элемент: 7
# 6. Проверяет: 7 >= 5? Да (True) → оставляет
# И так далее...

# Но filter() возвращает не список, а специальный объект
# Чтобы получить обычный список, нужно обернуть его в list()
# Поэтому мы пишем: list(filter(...))

# Что останется после фильтрации:
# Из [1, 4, 7, 3, 9, 10, 2, 6, 5, 8]
# Удаляются: 1, 4, 3, 2 (они меньше 5)
# Остаются: 7, 9, 10, 6, 5, 8

# Результат сохраняем обратно в переменную scores
# Теперь scores уже не исходный список, а отфильтрованный
scores = list(filter(lambda x: x >= 5, scores))

# Выводим результат фильтрации
print(scores)
# На экране будет: [7, 9, 10, 6, 5, 8]
# Обратите внимание: порядок сохранился, но элементов стало меньше

# ============================================
# ШАГ 4: ПОВЫШЕНИЕ ОЦЕНОК НА 1 БАЛЛ
# ============================================
# Теперь используем функцию map()
# Её задача: применить какую-то операцию к КАЖДОМУ элементу списка
# Синтаксис: map(функция-преобразование, список)

# Снова используем лямбда-функцию, но теперь другая:
# lambda x: min(x + 1, 10)
# Разбираем по частям:
# - "lambda x:" - создаём функцию с параметром x (оценка)
# - "x + 1" - увеличиваем оценку на 1 балл
# - "min(x + 1, 10)" - берём МЕНЬШЕЕ из двух чисел:
#   * x + 1 (увеличенная оценка)
#   * 10 (максимально возможная оценка)
#   Это нужно, чтобы оценка не стала больше 10

# Как работает map():
# 1. Берёт первый элемент отфильтрованного списка: 7
# 2. Применяет функцию: min(7 + 1, 10) = min(8, 10) = 8
# 3. Берёт второй элемент: 9
# 4. Применяет функцию: min(9 + 1, 10) = min(10, 10) = 10
# 5. Берёт третий элемент: 10
# 6. Применяет функцию: min(10 + 1, 10) = min(11, 10) = 10
#    Вот почему нужен min() - 10+1=11, но больше 10 нельзя!
# И так далее...

# map() тоже возвращает специальный объект
# Чтобы получить список, оборачиваем в list()

# Что получится после преобразования:
# Было: [7, 9, 10, 6, 5, 8]
# Стало: [8, 10, 10, 7, 6, 9]
# Проверим:
# 7 → 8, 9 → 10, 10 → 10 (осталось 10, не увеличилось)
# 6 → 7, 5 → 6, 8 → 9

# Сохраняем результат обратно в scores
scores = list(map(lambda x: min(x + 1, 10), scores))

# Выводим финальный результат
print(scores)
# На экране будет: [8, 10, 10, 7, 6, 9]

