# Цель: Работа с методами списков, сортировка, добавление/удаление элементов.
# Напишите программу, которая:
# 1.	Принимает от пользователя 5 оценок 
# (целые числа от 1 до 5)
# 2.	Создаёт список этих оценок
# 3.	Сортирует оценки по возрастанию
# 4.	Удаляет самую низкую и самую высокую оценки
# (первую и последнюю в отсортированном списке)
# 5.	Вычисляет средний балл из оставшихся оценок
# 6.	Выводит все этапы работы

# ============================================
# ШАГ 1: СОЗДАНИЕ ПУСТОГО СПИСКА
# ============================================
# Создаём пустой список с именем 'scores' (англ. "оценки")
# Список — это коллекция элементов, как коробка, в которую можно складывать вещи
# Квадратные скобки [] обозначают пустой список
scores = []

# ============================================
# ШАГ 2: ЗАПРОС 5 ОЦЕНОК У ПОЛЬЗОВАТЕЛЯ
# ============================================
# Цикл 'for' повторяет блок кода 5 раз (от 0 до 4)
# Переменная 'i' будет принимать значения: 0, 1, 2, 3, 4
for i in range(0, 5):
    # Внутри цикла:
    # 1. input() — показывает сообщение и ждёт, пока пользователь что-то введёт
    #    f'Введите оценку №{i} - ' — это f-строка, которая подставляет значение i в текст
    #    Если i=0, то будет: "Введите оценку №0 - "
    #    
    # 2. int(...) — преобразует введённый текст в целое число
    #    Пользователь вводит "5" (текст), а int() делает из этого число 5
    #    
    # 3. scores.append(...) — добавляет полученное число в конец списка 'scores'
    #    Метод .append() — это как положить предмет в конец коробки
    #    
    # Пример работы цикла:
    # Первый раз: i=0, добавляем первую оценку
    # Второй раз: i=1, добавляем вторую оценку
    # И так 5 раз
    scores.append(int(input(f'Введите оценку №{i} - ')))

# ============================================
# ШАГ 3: ВЫВОД ИСХОДНОГО СПИСКА
# ============================================
# После цикла у нас есть список из 5 оценок
# Выводим его на экран, чтобы увидеть, что получилось
# Пример вывода: [4, 2, 5, 3, 4]
print(scores)

# ============================================
# ШАГ 4: СОРТИРОВКА СПИСКА
# ============================================
# Метод .sort() сортирует элементы списка по возрастанию
# Он меняет сам список, а не создаёт новый
# Пример до: [4, 2, 5, 3, 4]
# Пример после: [2, 3, 4, 4, 5]
scores.sort()

# Выводим отсортированный список
print(scores)

# ============================================
# ШАГ 5: УДАЛЕНИЕ КРАЙНИХ ОЦЕНОК
# ============================================
# В отсортированном списке:
# - Первый элемент (индекс 0) — самая низкая оценка
# - Последний элемент (индекс -1) — самая высокая оценка

# Удаляем самую низкую оценку (первый элемент)
# Метод .pop(0) удаляет элемент с индексом 0 и возвращает его
# Мы не сохраняем возвращаемое значение, просто удаляем
scores.pop(0)

# Удаляем самую высокую оценку (последний элемент)
# После удаления первого элемента индексы меняются!
# Теперь последний элемент имеет индекс -1
# Метод .pop(-1) удаляет последний элемент
scores.pop(-1)

# Выводим список после удаления крайних оценок
# Если было [2, 3, 4, 4, 5], то станет [3, 4, 4]
print(scores)

# ============================================
# ШАГ 6: ВЫЧИСЛЕНИЕ СРЕДНЕГО БАЛЛА
# ============================================
# Средний балл = сумма всех оценок / количество оценок

# 1. sum(scores) — встроенная функция sum() складывает все элементы списка
#    Для списка [3, 4, 4] сумма будет: 3 + 4 + 4 = 11
#    
# 2. len(scores) — встроенная функция len() возвращает длину списка
#    Для списка [3, 4, 4] длина будет: 3
#    
# 3. Деление / — обычное математическое деление
#    11 / 3 = 3.666666...
#    
# Результат выводится сразу, без сохранения в переменную
print(sum(scores) / len(scores))
