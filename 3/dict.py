# ============================================
# СПОСОБ 1: Использование фигурных скобок {}
# Самый распространённый и простой способ
# ============================================

# Пустой словарь
empty_dict = {}
print("1. Пустой словарь:", empty_dict)

# Словарь с начальными данными
student = {
    "имя": "Анна",
    "возраст": [20,123,123,123,123],
    "курс": 2,
    "средний_балл": 4.5
}
print("\n2. Словарь студента:", student)

# Словарь с разными типами данных
mixed_dict = {
    "строка": "текст",
    "число": 42,
    "список": [1, 2, 3],
    "вложенный_словарь": {"ключ": "значение"}
}
print("\n3. Смешанный словарь:", mixed_dict)


# ============================================
# СПОСОБ 2: Функция dict() с парами ключ=значение
# ============================================

# Создание словаря из именованных аргументов
car = dict(марка="Toyota", модель="Camry", год=2022)
print("\n4. Словарь автомобиля (dict()):", car)

# Пустой словарь через dict()
empty_dict2 = dict()
print("5. Пустой словарь (dict()):", empty_dict2)


# ============================================
# СПОСОБ 3: Функция dict() со списком кортежей
# Каждый кортеж = (ключ, значение)
# ============================================

# Список кортежей (ключ, значение)
pairs = [("яблоко", 100), ("банан", 50), ("апельсин", 80)]
fruits = dict(pairs)
print("\n6. Словарь фруктов из списка кортежей:", fruits)

# Аналогично со списком списков
pairs_list = [["математика", 5], ["физика", 4], ["химия", 3]]
grades = dict(pairs_list)
print("7. Словарь оценок из списка списков:", grades)


# ============================================
# СПОСОБ 4: Функция dict() с zip()
# Объединение двух списков в словарь
# ============================================

keys = ["name", "age", "city"]
values = ["Иван", 25, "Москва"]

person = dict(zip(keys, values))
print("\n8. Словарь из двух списков (zip()):", person)


# ============================================
# СПОСОБ 5: Метод fromkeys()
# Создание словаря с одинаковым значением для всех ключей
# ============================================

# Создаём ключи из списка, всем присваиваем значение 0
default_scores = dict.fromkeys(["math", "physics", "history"], 0)
print("\n9. Словарь с дефолтными значениями (fromkeys()):", default_scores)

# Без указания значения - все значения будут None
empty_values = dict.fromkeys(["login", "password", "email"])
print("10. Словарь со значениями None:", empty_values)


# ============================================
# СПОСОБ 6: Словарное включение (dict comprehension)
# Аналогично списковым включениям, но для словарей
# ============================================

# Создание словаря квадратов чисел
squares = {x: x**2 for x in range(1, 6)}
print("\n11. Словарь квадратов (dict comprehension):", squares)

# Создание словаря из списка с условием
names = ["Анна", "Борис", "Виктор", "Галина"]
name_lengths = {name: len(name) for name in names if len(name) > 4}
print("12. Словарь длин имён (с условием):", name_lengths)


# ============================================
# СПОСОБ 7: Постепенное добавление элементов
# ============================================

# Создаём пустой словарь и добавляем элементы по одному
book = {}
book["название"] = "Мастер и Маргарита"
book["автор"] = "Булгаков"
book["год"] = 1967
book["жанр"] = "роман"

print("\n13. Словарь книги (постепенное добавление):", book)


# ============================================
# СПОСОБ 8: Из другого словаря (копирование)
# ============================================

original = {"a": 1, "b": 2, "c": 3}
copy_dict = dict(original)  # или original.copy()
print("\n14. Копия словаря:", copy_dict)


# ============================================
# ПРИМЕРЫ ДОСТУПА К ЭЛЕМЕНТАМ
# ============================================

print("\n" + "="*50)
print("ПРИМЕРЫ РАБОТЫ СО СЛОВАРЁМ:")

# Создаём тестовый словарь
user = {
    "username": "ivan_petrov",
    "email": "ivan@example.com",
    "age": 30,
    "is_active": True,
    "skills": ["Python", "SQL", "Git"]
}

# Доступ к значению по ключу
print("1. Имя пользователя:", user["username"])

# Безопасный доступ (не вызывает ошибку при отсутствии ключа)
print("2. Город (безопасно):", user.get("city", "не указан"))

# Изменение значения
user["age"] = 31
print("3. Новый возраст:", user["age"])

# Добавление нового ключа
user["city"] = "Москва"
print("4. Добавлен город:", user["city"])

# Получение всех ключей
print("5. Все ключи:", list(user.keys()))

# Получение всех значений
print("6. Все значения:", list(user.values()))

# Получение пар (ключ, значение)
print("7. Все пары:", list(user.items()))

# Проверка наличия ключа
if "email" in user:
    print("8. Email есть в словаре:", user["email"])

# Удаление элемента
removed_value = user.pop("is_active", False)
print("9. Удалён ключ is_active, значение было:", removed_value)
print("10. Обновлённый словарь:", user)