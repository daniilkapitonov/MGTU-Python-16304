#Написать задание исползуя словари.
#У вас есть список вещей, где имя вещи - ключ, вес - значение
# item = {"кружка":100,"фонарь":230}x
#Задача - турист собирает рюкзак в поход. 
# Программа предлагает его список вещей
#которые доступны к взятию. Турист выбирает 
# вещь и добавляет в рюкзак
#НО максимальный вес рюкзака 3 кг. 
# Если новая вещь превышет, то писать ошибку

# 1. Создаём словарь под названием "items" (предметы)
# Ключи — названия предметов (строки), значения — их вес в граммах (числа)
items = {
    "кружка": 100,      # кружка весит 100 грамм
    "фонарь": 230,      # фонарь весит 230 грамм
    "спальник": 200,    # спальник весит 200 грамм
    "палатка": 1500,    # палатка весит 1500 грамм
    "термос": 500,      # термос весит 500 грамм
    "спички": 50,       # спички весят 50 грамм
    "топор": 800,       # топор весит 800 грамм
    "дождевик": 100     # дождевик весит 100 грамм
}

# 2. Создаём переменную "veight" (видимо, опечатка, должно быть "weight" — вес)
# Изначально рюкзак пустой, поэтому вес = 0
veight = 0

# 3. Начинаем бесконечный цикл "while True:"
# Цикл будет выполняться вечно, пока мы не остановим программу вручную (Ctrl+C)
# Или пока не добавим условие для выхода
while True:
    
    # 4. Выводим на экран все доступные предметы (только названия, без веса)
    # Метод .keys() возвращает список всех ключей словаря (названия предметов)
    print(items.keys())
    
    # 5. Запрашиваем у пользователя название предмета, который он хочет положить в рюкзак
    # Функция input() показывает сообщение и ждёт, пока пользователь что-то введёт
    item = input("Введите название предмета - ")
    
    # 6. Проверяем, существует ли такой предмет в нашем словаре
    # "if item in items.keys():" значит: если введённое название есть среди ключей словаря
    if item in items.keys():
        
        # 7. Если предмет существует, проверяем, поместится ли он в рюкзак
        # Сравниваем: текущий вес рюкзака + вес предмета <= максимальная вместимость (3000 грамм)
        if veight + items[item] <= 3000:
            
            # 8. Если предмет помещается, добавляем его вес к общему весу рюкзака
            veight += items[item]  # Это то же самое, что veight = veight + items[item]
            
            # 9. Выводим информацию о добавленном предмете и новом весе рюкзака
            # f-строка (строка с буквой f перед кавычками) позволяет вставлять переменные в текст
            print(f'Вес предмета {items[item]} вес рюкзака {veight}')
        
        else:
            # 10. Если предмет НЕ помещается (вес превысит 3000), выводим сообщение
            print("Рюкзак не может вместить это", veight)
    
    else:
        # 11. Если введённого предмета нет в словаре, выводим сообщение об ошибке
        print("Нет такого предмета")
    
    # 12. Цикл завершается и начинается снова с шага 4
    # Программа снова покажет список предметов и спросит, что добавить